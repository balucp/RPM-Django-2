class EarlyWarningScore1:

    def __init__(self, data):
        """initialize parameters

        Args:
            data (dict): data consists of vitals
        """

        required_keys = ["rr", "hr", "spo2", "bp_sys", "temp", "supplemental_oxygen", "avpu"]
        if not any(key in data and data[key] is not None for key in data):
            raise ValueError("At least one required element must be present")

        self.data = {
            "rr": data.get("rr", None),
            "hr": data.get("hr", None),
            "spo2": data.get("spo2", None),
            "bp_sys": data.get("bp_sys", None),
            "temp": data.get("temp", None),
            "supplemental_oxygen": data.get("supplemental_oxygen", None),
            "avpu": data.get("avpu", None),
        }


    def compute_news(
        self,
        rr: None,
        hr: None,
        spo2: None,
        temp: None,
        bp_sys=None,
        supplemental_oxygen=None,
        avpu=None,
    ):
        """compute EWS using NEWS method
        reference: https://www.mdcalc.com/national-early-warning-score-news

        Args:
            rr (int): respiration rate
            hr (int): heart rate
            spo2 (int): oxygen saturation
            temp (float): body temperature
            bp_sys (int, optional): systolic blood pressure. Defaults to None.
            supplemental_oxygen (str, optional): supplemental oxygen. Defaults to None.
            avpu (str, optional): conciousness. Defaults to None.

        Raises:
            ValueError: _description_

        Returns:
            int: NEWS score
        """

        score = 0

        # compute score for RR
        if rr is None:
            score += 0
        elif rr < 0:
            raise ValueError(f"Invalid rr value ({rr})")
        elif (rr <= 8) or (rr >= 25):
            score += 3
        elif (rr >= 9) and (rr <= 11):
            score += 1
        elif (rr >= 12) and (rr <= 20):
            score += 0
        elif (rr >= 21) and (rr <= 24):
            score += 2
        else:
            raise ValueError(f"Invalid rr value ({rr})")

        # compute score for spo2
        if spo2 is None:
            score += 0
        elif spo2 < 0:
            raise ValueError(f"Invalid spo2 value ({spo2})")
        elif spo2 <= 91:
            score += 3
        elif (spo2 >= 92) and (spo2 <= 93):
            score += 2
        elif (spo2 >= 94) and (spo2 <= 95):
            score += 1
        elif spo2 >= 96:
            score += 0
        else:
            raise ValueError(f"Invalid spo2 value ({spo2})")

        # compute score for temp
        if temp is None:
            score += 0
        elif temp < 0:
            raise ValueError(f"Invalid temp value ({temp})")
        elif temp <= 35.0:
            score += 3
        elif (temp >= 35.1) and (temp <= 36.0):
            score += 1
        elif (temp >= 36.1) and (temp <= 38.0):
            score += 0
        elif (temp >= 38.1) and (temp <= 39.0):
            score += 1
        elif temp >= 39.1:
            score += 2
        else:
            raise ValueError(f"Invalid temp value ({temp})")

        # compute score for hr
        if hr is None:
            score += 0
        elif hr < 0:
            raise ValueError(f"Invalid hr value ({hr})")
        elif hr <= 40:
            score += 3
        elif (hr >= 41) and (hr <= 50):
            score += 1
