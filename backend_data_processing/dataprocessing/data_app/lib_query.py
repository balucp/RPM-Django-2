def calculate_stat_np1Darray(X, val_replace_NaN, round_number, method='median'):
    if X is None or X.size == 0:
        return val_replace_NaN, val_replace_NaN
        
    X = X.astype(float)
    try:
        X = X[~np.isnan(X)]
    except:
        pass

    if len(X) == 0:
        y_val = val_replace_NaN
        y_sd = val_replace_NaN
    else:
        if method == 'mean':
            y_val = np.mean(X)
        elif method == 'sum':
            y_val = np.sum(X)
        elif method == 'median':
            y_val = np.median(X)

        y_sd = np.std(X)
    
    if round_number:
        y_val = roundNumber(y_val)
        y_sd = roundNumber(y_sd)
    return y_val, y_sd


def cal_data_length(dateTime, resolution, startDateTime):
    trends_vital_sign_minutes_resolution = settings.trends_vital_sign_minutes_resolution
    
    if resolution == 'hourly':
        stopDateTime = dateTime
        data_length = math.ceil((stopDateTime - startDateTime).total_seconds() / 3600)

    elif resolution == 'daily':
        stopDateTime = dateTime
        data_length = (stopDateTime - startDateTime).days + 1

    elif resolution == 'minutes':
        stopDateTime = dateTime
        data_length = math.ceil((stopDateTime - startDateTime).total_seconds()/60)
        data_length = math.ceil(data_length/trends_vital_sign_minutes_resolution)

    return startDateTime, stopDateTime, data_length


