class calculate_ews():
    # https://www.mdcalc.com/national-early-warning-score-news
    def __init__(self, method, input, threshold_score, val_replace_NaN):
        self.method = method
        self.score = 0
        self.flag = 0
        self.flag_notification = ''
        self.threshold_score = threshold_score
        self.val_replace_NaN = val_replace_NaN
        
        try:
            self.RR = input['RR']
        except Exception as e:
            pass

        try:
            self.HR = input['HR']
        except Exception as e:
            pass
            
        try:
            self.SpO2 = input['SpO2']
        except Exception as e:
            pass
            
            
        try:
            self.BP_sys = input['BP_Sys']
        except Exception as e:
            pass
            
            
        try: 
            self.temperature = input['Temperature']
        except Exception as e:
            pass
    


    def NEWS(self):
        def return_resp_rate(resp_rate):
            if 8.1 <= resp_rate <= 11.9:
                return 1
            elif resp_rate <= 8:
                return 3
            elif 21 <= resp_rate <= 24.9:
                return 2
            elif resp_rate >= 25:
                return 3
            else:
                return 0

        def return_oxy(oxy_sat):
            if oxy_sat <= 91:
                return 3
            elif 91.1 <= oxy_sat <= 93:
                return 2
            elif 93.1 <= oxy_sat <= 95.9:
                return 1
            else:
                return 0
        
        def return_temperature(temp):
            if temp <= 35:
                return 3
            elif 35.1 <= temp <= 36:
                return 1
            elif 36.1 <= temp <= 38:
                return 0
            elif 38.1 <= temp <= 39:
                return 1
            elif temp >= 39.1:
                return 2

        def return_sys_blood_pres(sys_blood_pres):
            if sys_blood_pres <= 90.9:
                return 3
            elif 91 <= sys_blood_pres <= 100.9:
                return 2
            elif 101 <= sys_blood_pres <= 110.9:
                return 1
            elif 111 <= sys_blood_pres <= 219.9:
                return 0
            elif sys_blood_pres >= 220:
                return 3

        def return_heart_rate(heart_rate):
            if heart_rate <= 40.9:
                return 3
            elif 41 <= heart_rate <= 50.9:
                return 1
            elif 51 <= heart_rate <= 90.9:
                return 0
            elif 91 <= heart_rate <= 110.9:
                return 1
            elif 111 <= heart_rate <= 130.9:
                return 2
            elif heart_rate >= 131:
                return 3
        

        data_available = False
        flag_notification = ''
        flag = 0


        try:
            score_rr = return_resp_rate(self.RR)
            if score_rr==3:
                flag_notification = flag_notification + "RR is abnormal. "
                flag = 1
            data_available = True
        except:
            score_rr = 0
        


        try:
            score_hr = return_heart_rate(self.HR)
            if score_hr==3:
                flag_notification = flag_notification + "HR is abnormal. "
                flag = 1
            data_available = True
        except:
            score_hr = 0


        try:
            score_spo2 = return_oxy(self.SpO2)
            if score_spo2==3:
                flag_notification = flag_notification + "SpO2 is abnormal. "
                flag = 1
            data_available = True
        except:
            score_spo2 = 0


        try:
            score_temperature = return_temperature(self.temperature)
            if score_temperature==3:
                flag_notification = flag_notification + "Temperture is abnormal. "
                flag = 1
            data_available = True
        except:
            score_temperature = 0


        try:
            score_sys = return_sys_blood_pres(self.BP_sys)
            if score_sys==3:
                flag_notification = flag_notification + "Blood Pressure (Systolic) is abnormal. "
                flag = 1
            data_available = True
        except:
            score_sys = 0


        score = score_rr + score_hr + score_spo2 + score_temperature + score_sys

        if data_available==False:
            score = self.val_replace_NaN

        if score>self.threshold_score:
            flag_notification = flag_notification + "EWS score is high. "
            flag = 1
        
        return score, flag, flag_notification

    
    def get_score(self):
        if self.method=='NEWS':
            self.score, self.flag, self.flag_notification = self.NEWS()


def calculate_breathing_index(x, val_replace_NaN):
    
    if (x>30) or (x<9):
        output = 'Bad'
    elif (x>=21) and (x<=29):
        output = 'Moderate'
    elif (x>=15) and (x<=20):
        output = 'Good'
    elif (x>=9) and (x<=14):
        output = 'Very Good'
    else:
        output = '--'
    
    if x==val_replace_NaN:
        output = '--'

    
    return output


def calculate_compliance(wear_rr, wear_hr, min_wear_rr, min_wear_hr):

    if wear_rr>min_wear_rr:
        wear_rr = min_wear_rr
    
    if wear_hr>min_wear_hr:
        wear_hr = min_wear_hr

    

    min_total_wear = min_wear_rr + min_wear_hr

    compliance = (wear_hr + wear_rr)/min_total_wear
    compliance = compliance * 100
    if compliance>100:
        compliance = 100
    
    compliance = int(compliance)
    return compliance


