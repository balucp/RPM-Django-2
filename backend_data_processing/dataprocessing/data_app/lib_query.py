def handle_spot_trend_query(
    data_processing, 
    desired_attr,
    bool_remove_bad_data=True,
    bool_keep_bad_data_as_NaN=False,
    replace_NaN_with_value=True,
    add_sqa_to_trend=False,
    add_skin_status_to_trend=False
    ):

    items = data_processing.values()

    if items:
        # remove bad data
        if bool_remove_bad_data:
            items = common.remove_bad_data(items, bool_keep_bad_data_as_NaN)

        if not settings.BOOL_DISPLAY_HR_FROM_CHEST_IN_TREND_MINUTES:
            items = common.remove_hr_from_chest(items)

    output = {}
    df = pd.DataFrame(items)
    # check --> if any of desired_attr does not exist, insert that attr with NaN
    columns =list(set(df.columns).union(desired_attr))
    df = df.reindex(columns=sorted(columns))

    def format_date(x):
        return x.strftime("%Y-%m-%d %H:%M:%S")
    
    if not df.empty:

        df['listtime'] = df['date_time'].apply(format_date)

        # add temperature to `desired_attr`
        desired_attr = desired_attr  # TODO: Is this needed? If not, remove

        # filter columns
        df_metrics = df[['listtime'] + desired_attr]

        if add_sqa_to_trend:
            try:
                df_metrics['signal_quality_status'] = df['signal_quality_status']
            except Exception as e:
                logging.error(f"column signal_quality_status is missing, assign NaN. error message is {e}.")
                df_metrics['signal_quality_status'] = np.nan
        if add_skin_status_to_trend:
            try:
                df_metrics['sensor_onskin_status'] = df['sensor_onskin_status'].map({1: 'Good', 0: 'Bad'})
            except Exception as e:
                logging.error(f"column sensor_onskin_status is missing, assign NaN. error message is {e}.")
                df_metrics['sensor_onskin_status'] = 0
                      
        # calculate mean
        df_metrics_overall_mean = df_metrics.mean(axis=0)
        df_metrics_overall_mean[~df_metrics_overall_mean.notnull()] = settings.val_replace_NaN # replace NaN with -1

        # List of columns to exclude from conversion of Decimal to numberic
        exclude_columns = ['sensor_onskin_status', 'signal_quality_status', 'listtime', 'dashboard_mode']
       
        # Convert data type from Decimal to numeric for desired attributes
        for column in df_metrics.columns:
            if column not in exclude_columns:
                df_metrics[column] = pd.to_numeric(df_metrics[column], errors='coerce')

        # replace NaN with -1
        if replace_NaN_with_value:
            df_metrics.fillna(settings.val_replace_NaN, inplace=True)

        # insert 0 SD (because it is SPOT data)
        df_metrics_SD = df_metrics.copy()
        df_metrics_SD[desired_attr] = 0

        output['metrics'] = df_metrics.to_dict('list')
        # output['metrics_SD'] = df_metrics_SD.to_dict('list')
        output['metrics_overall_mean'] = df_metrics_overall_mean.to_dict()      


        """ update SD for RR, HR, and SpO2 """
        list_attr_sd_spot = ['rr_sd', 'hr_sd', 'spo2_sd'] # SD columns in database.
        var_map_spot_and_metric_sd = { # SD columns in database
            "rr": "rr_sd",
            "hr": "hr_sd",
            "spo2": "spo2_sd",
        }
        
        # check if SD columns are available, add column with NaN
        for x_attr in list_attr_sd_spot:
            if x_attr not in list(df.columns):
                df[x_attr] = np.nan
                logging.warning(f"missing variable {x_attr} when running function handle_spot_trend_query(). create a column with NaN.")
        
        # add SD for RR, HR, SpO2
        for x_attr in list(var_map_spot_and_metric_sd.keys()):
            current_key_value = var_map_spot_and_metric_sd[x_attr]
            df_metrics_SD[x_attr] = df[current_key_value]
        
        # Convert data type from Decimal to numeric for desired attributes
        for column in df_metrics_SD.columns:
            if column not in exclude_columns:
                df_metrics_SD[column] = pd.to_numeric(df_metrics_SD[column], errors='coerce')

        # replace NaN with -1
        if replace_NaN_with_value:
            df_metrics_SD.fillna(settings.val_replace_NaN, inplace=True)
        output['metrics_SD'] = df_metrics_SD.to_dict('list')

    else:
        output['metrics'] = {col: [] for col in columns + ['listtime']}
        output['metrics_SD'] = {col: [] for col in columns + ['listtime']}
        output['metrics_overall_mean'] = {col: [] for col in columns}

    return output



def query_health_input(user_id, start_datetime, end_datetime, resolution, operation):
    """
    Query health data for a specific user within a date range and resolution, 
    and perform statistical operations (median or standard deviation).

    Args:
        user_id (int): user id
        start_datetime (datetime.datetime): start date and time
        end_datetime (datetime.datetime): end date and time
        resolution (str): ["minutes", "hourly", "daily"]
        operation (str): ["median", "sd"]

    Returns:
        pandas.DataFrame: output data frame
    """
    if start_datetime is None or end_datetime is None:
        query_items = HealthData.objects.filter(user_id=user_id).order_by('-datetime')
    else:
        query_items = HealthData.objects.filter(
            Q(user_id=user_id) & Q(datetime__range=(start_datetime, end_datetime))
        ).order_by('-datetime')

    df_query_items = pd.DataFrame.from_records(
        query_items.values(
            "datetime", "bp_sys", "bp_dia", "rr", "hr", "spo2", "body_temp", "weight", "blood_sugar"
        )
    )

    # output columns
    map_columns_rename={
                "datetime": "listtime",
                "bp_sys": "BP_Sys",
                "bp_dia": "BP_Dia",
                "rr": "RR_manual",
                "hr": "HR_manual",
                "spo2": "SpO2_manual",
                "body_temp": "body_temp_manual",
                "weight": "weight_manual",
                "blood_sugar": "blood_sugar_manual"
            }

    map_columns_type={
                "BP_Sys": int,
                "BP_Dia": int,
                "RR_manual": int,
                "HR_manual": int,
                "SpO2_manual": int,
                "body_temp_manual": float,
                "weight_manual": float,
                "blood_sugar_manual": float
            }
    cols_output = list(map_columns_rename.values())

    if len(query_items) > 0:
        # convert items to df
        # df_query_items = pd.DataFrame(query_items)

        # rename columns
        df_query_items = df_query_items.rename(
            columns = map_columns_rename
        )

        # Convert columns based on the map
        for col, dtype in map_columns_type.items():
            if col not in df_query_items.columns: # impute missing columns with NaN
                df_query_items[col] = np.nan
            else:
                df_query_items[col] = pd.to_numeric(df_query_items[col], downcast='integer' if dtype == int else 'float', errors='coerce')
            
            # If dtype is float, round to 1 decimal place
            if dtype == float:
                df_query_items[col] = df_query_items[col].apply(lambda x: round(x, 1))

        # keep columns
        df_query_items = df_query_items[cols_output]

        # sort date and time
        df_query_items = df_query_items.sort_values("listtime")

        # calculate the median value, resample data in hourly or daily
        df_query_items["listtime"] = pd.to_datetime(df_query_items["listtime"])
        df_query_items["listtime"] = df_query_items["listtime"].dt.tz_localize(None)
        df_query_items.set_index("listtime", inplace=True)
        if start_datetime:
            start_datetime = start_datetime.replace(tzinfo=None)

        if resolution == "hourly":
            if operation == "median":
                resampled = df_query_items.resample("H").median().reset_index()
            elif operation == "sd":
                resampled = (
                    df_query_items.resample("H").std(ddof=0).reset_index()
                )  # Population standard deviation

            df_query_items = resampled

        elif resolution == "daily":
            if operation == "median":
                resampled = (
                    df_query_items.resample("24H", origin=start_datetime)
                    .median()
                    .reset_index()
                )
            elif operation == "sd":
                resampled = (
                    df_query_items.resample("24H", origin=start_datetime)
                    .std(ddof=0)
                    .reset_index()
                )  # Population standard deviation

            df_query_items = resampled

        elif resolution == "minutes":
            df_query_items = df_query_items.reset_index()


        if (resolution in ["hourly", "daily"]) and (operation == "median"):
            # if mod value is lower than 0.5, floor the value
            # if mod value is equal or greater than 0.5, ceil the value
            for col, dtype in map_columns_type.items():
                if dtype is int:
                    df_query_items[col] = df_query_items[col].apply(lambda x: np.floor(x) if (x % 1) < 0.5 else np.ceil(x))


        if resolution == "hourly":
            df_query_items["listtime"] = df_query_items["listtime"].dt.strftime(
                "%Y-%m-%d %H:%M"
            )
        elif resolution in [
            "daily",
            "minutes",
        ]:  # TODO: consider returning only yyyy-dd-mm in the daily resolution
            df_query_items["listtime"] = df_query_items["listtime"].dt.strftime(
                "%Y-%m-%d %H:%M:%S"
            )

            if resolution == "daily":
                df_query_items["listdate"] = df_query_items["listtime"]

    else:
        # no health input
        df_query_items = pd.DataFrame(columns=cols_output)

    # validate that output response consist of columns listtime, BP_Sys, BP_Dia
    assert set(cols_output).issubset(set(df_query_items.columns))

    return df_query_items  # dict_health_input


