def extract_items_in_range_and_remove_from_original(X, startdate, stopdate, datetime_format='%Y-%m-%d %H:%M:%S'):
    if len(X) > 0:
        X_df = pd.DataFrame.from_dict(X)
        X_df['date_time'] = pd.to_datetime(X_df['date_time'])
        timezone = X_df['date_time'].dt.tz
        startdate = pd.to_datetime(startdate, format=datetime_format).tz_convert(timezone)
        stopdate = pd.to_datetime(stopdate, format=datetime_format).tz_convert(timezone)
        y_df = X_df.loc[(X_df['date_time'] >= startdate) & (X_df['date_time'] <= stopdate)]
        y_index = y_df.index.values.tolist()
        y = [X[i] for i in y_index]

        Xnew_index = (X_df.drop(y_df.index)).index
        if len(Xnew_index) > 0:
            Xnew = [X[i] for i in Xnew_index]
        else:
            Xnew = y
    else:
        Xnew = []
        y = []

    return Xnew, y


