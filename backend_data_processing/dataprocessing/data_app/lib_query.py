def get_latest_datetime(query_response, datetime_key):
    last_sync_datetime = getattr(query_response.first(), datetime_key)

    for i_items in range(1, len(query_response)):

        current_datetime_lastsync = getattr(query_response[i_items], datetime_key)
        
        if current_datetime_lastsync>last_sync_datetime:
            last_sync_datetime = current_datetime_lastsync

    return str(last_sync_datetime)


