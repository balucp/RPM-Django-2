def SQA_decision(sqa_val, threshold):
    # if np.isnan(sqa_val):
    #     sqa_val = 0

    if ~np.isnan(sqa_val):
        # sqa_val = 0

        if (sqa_val<threshold[0]):
            output_sqa = 0
        elif (sqa_val>=threshold[0]) & (sqa_val<threshold[1]):
            output_sqa = 1
        elif (sqa_val>=threshold[1]):
            output_sqa = 2
    else:
        output_sqa = np.nan
    return output_sqa


